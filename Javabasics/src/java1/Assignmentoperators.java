package java1;

public class Assignmentoperators {

	public static void main(String[] args) {
		
		
		int a =10;
		
		a += 5; //a=a+5 a=10+5
		
		System.out.println("The value of a is: "+ a);// 15 
		
		a -=2;  //a=a-2
		
		System.out.println("The value of a after - 2 is: "+ a);
		
		a *=5;  //a=a*2 
		System.out.println("The value of a after mul with 5  is: "+ a);
		
		a /= 2; //a=a/2
		System.out.println("The value of a after divided by 2 is: "+ a);
		
		
		
		a %= 8; //a=a%8
		
		System.out.println("The value of a after % 8  is: "+ a);
		
		
	}

}
