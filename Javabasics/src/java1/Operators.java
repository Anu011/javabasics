package java1;

public class Operators {

	public static void main(String[] args) {
		
		//This program is demontrating Unary operators
		
		//increment operator 
		int i=10;
		i++;
		System.out.println(i);
		
		i--;//decrement operator
		System.out.println(i);
		
		--i; //pre_decrement
		System.out.println(i);
		++i; //pre_incremet
		System.out.println(i);
		

	}

}
